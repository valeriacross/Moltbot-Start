            f"hair spread around head, hair fanned out, dark flowing locks, wavy dark hair, hair against background. "
            f"Replace with: short silver Italian cut, slightly voluminous top, nape exposed.\n\n"
            f"2. GLASSES MANDATORY: Valeria Cross ALWAYS wears thin octagonal Vogue Havana dark tortoiseshell glasses. "
            f"If the prompt does NOT mention glasses or eyeglasses, ADD this phrase in the Facial identity section: "
            f"'thin octagonal Vogue Havana dark tortoiseshell frame eyeglasses (MANDATORY, always present)'. "
            f"Also ensure 'no glasses' does NOT appear anywhere in the prompt.\n\n"
            f"2b. GLASSES POSE REMOVAL: Remove ANY description of hands or fingers touching, adjusting, holding or "
            f"raising to the glasses or temple area. The glasses are simply worn — no hand interaction with them.\n\n"
            f"3. NEGATIVE PROMPTS CONFLICTS: Scan the negative prompts section. "
            f"Remove from negatives any term that contradicts a positive element in the prompt.\n\n"
            f"3b. MIRROR SELFIE RULE: If the prompt contains selfie, mirror, mirror selfie, bathroom, restroom, reflection — "
            f"smartphone held in hand IS a required prop. Ensure it is present as positive element and removed from negatives.\n\n"
            f"4. SUBJECT BLEED: Remove any physical description belonging to the original reference subject. "
            f"Valeria Cross DNA: Male Italian face 60yo, silver beard 6-7cm, octagonal Vogue glasses, "
            f"silver short hair, hourglass body, smooth skin.\n\n"
            f"5. WATERMARK TEXT: Must read exactly: 'feat. Valeria Cross 👠' "
            f"in elegant champagne cursive, very small, bottom center/left, 90% opacity. "
            f"Replace any other watermark text with the exact text above.\n\n"
            f"6. NAME REMOVAL: Remove any occurrence of 'Valeria Cross' or 'DNA of Valeria Cross' from the prompt body.\n\n"
            f"7. KEEP INTACT: scene, outfit, lighting, environment, pose, mood, camera settings, "
            f"photographic style, watermark spec, all creative elements not related to the subject's identity.\n\n"
            f"8. BODY HAIR ENFORCEMENT: Ensure Subject body section explicitly states: "
            f"'completely hairless body', 'no body hair, no chest hair, no arm hair, no leg hair', "
