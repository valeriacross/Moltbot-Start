@bot.message_handler(content_types=['photo'])
def handle_photo(m):
    uid = m.from_user.id
    if not is_allowed(uid):
        logger.warning(f"🚫 Foto non autorizzata: uid={uid}")
        return
    if user_mode.get(uid) != "photo":
        bot.send_message(m.chat.id,
            "Scegli prima la modalità:",
            reply_markup=get_mode_kb())
        return
    username = m.from_user.username or m.from_user.first_name
    try:
        file_info = bot.get_file(m.photo[-1].file_id)
        img_bytes = bot.download_file(file_info.file_path)
        logger.info(f"🖼️ Foto da {username} (id={uid}), {len(img_bytes)} bytes")
    except Exception as e:
        bot.reply_to(m, f"❌ Errore download foto: {html.escape(str(e))}")
        return

    wait = bot.send_message(m.chat.id,
        "🔍 <b>Analisi immagine in corso...</b>\n⏳ Attendi ~20-30 secondi.")

    outfit_desc, analyze_err = analyze_scene(img_bytes, gemini)
    if analyze_err:
        logger.warning(f"   ⚠️ analyze_scene: {analyze_err}")

    def task_single():
        try:
            logger.info(f"   🖼️ task_single avviato")
            raw = generate_from_image(img_bytes, outfit_desc)
            logger.info(f"   📝 generate completato ({len(raw)} chars)")
            reviewed = review_and_fix(clean_output(raw))
            certified = clean_output(reviewed)
            try:
                bot.delete_message(m.chat.id, wait.message_id)
            except Exception:
                pass
            if "⚠️ Risposta vuota" in certified or "ERRORE API" in certified:
                bot.send_message(m.chat.id,
                    f"❌ <b>Generazione fallita.</b>\n\n<code>{html.escape(certified)}</code>",
                    reply_markup=get_mode_kb())
                return
            send_prompt(m.chat.id, uid, certified, img_bytes)
            logger.info(f"✅ Prompt foto inviato a {username} ({len(certified)} chars)")
        except Exception as e:
            logger.error(f"   ❌ task_single crash: {e}", exc_info=True)
            try:
                bot.delete_message(m.chat.id, wait.message_id)
            except Exception:
                pass
            bot.send_message(m.chat.id, f"❌ Errore generazione:\n<code>{html.escape(str(e))}</code>")

    executor.submit(task_single)

