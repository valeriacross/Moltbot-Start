@bot.callback_query_handler(func=lambda c: c.data.startswith("mode_") or c.data.startswith("action_"))
def handle_callbacks(c):
    cid = c.message.chat.id
    uid = c.from_user.id
    if not is_allowed(uid):
        logger.warning(f"🚫 Callback non autorizzato: uid={uid}")
        return
    username = c.from_user.username or c.from_user.first_name
    try:
        bot.answer_callback_query(c.id)
    except Exception:
        pass

    if c.data == "action_miniprompt":
        prompt = last_prompt.get(uid)
        if not prompt:
            bot.send_message(cid, "⚠️ Nessun prompt disponibile.")
            return
        wait = bot.send_message(cid, "📋 <b>Genero il mini prompt...</b>")
        mini, err = generate_mini_prompt(prompt, gemini)
        try: bot.delete_message(cid, wait.message_id)
        except Exception: pass
        bot.send_message(cid, mini if mini else err or "❌ Errore.", parse_mode="HTML")
        return

    if c.data == "action_minicaption":
        prompt = last_prompt.get(uid)
        if not prompt:
            bot.send_message(cid, "⚠️ Nessun prompt disponibile.")
            return
        wait = bot.send_message(cid, "📝 <b>Genero la mini caption...</b>")
        cap, err = generate_mini_caption(prompt, gemini)
        try: bot.delete_message(cid, wait.message_id)
        except Exception: pass
        bot.send_message(cid, cap if cap else err or "❌ Errore.", parse_mode="HTML")
        return

    if c.data == "mode_text" or c.data == "action_text":
        user_mode[uid] = "text"
        logger.info(f"✏️ {username} → modalità testo")
        try:
            bot.edit_message_reply_markup(cid, c.message.message_id, reply_markup=None)
        except Exception:
            pass
        bot.send_message(cid, "✏️ Modalità <b>Testo</b>.\n\nDescrivi la scena o l'outfit.", parse_mode="HTML")

    elif c.data == "mode_photo" or c.data == "action_photo":
        user_mode[uid] = "photo"
        logger.info(f"📸 {username} → modalità foto")
        try:
            bot.edit_message_reply_markup(cid, c.message.message_id, reply_markup=None)
        except Exception:
            pass
        bot.send_message(cid, "📸 Modalità <b>Foto</b>.\n\nInvia la foto.", parse_mode="HTML")

# --- PULIZIA OUTPUT ---
def clean_output(text):
    lines = text.splitlines()
    skip_patterns = [
        "ok, here is", "here is", "here's", "sure, here", "certainly",
        "of course", "below is", "the following", "as requested",
        "i've created", "i have created", "this is"
    ]
    while lines and any(lines[0].strip().lower().startswith(p) for p in skip_patterns):
        lines.pop(0)
    while lines and not lines[0].strip():
        lines.pop(0)
    return "\n".join(lines)

