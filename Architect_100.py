@bot.message_handler(content_types=['text'])
def handle_text(m):
    if m.text and m.text.startswith('/'):
        return
    uid = m.from_user.id
    if not is_allowed(uid):
        logger.warning(f"🚫 Testo non autorizzato: uid={uid}")
        return
    if user_mode.get(uid) != "text":
        bot.send_message(m.chat.id,
            "Scegli prima la modalità:",
            reply_markup=get_mode_kb())
        return
    username = m.from_user.username or m.from_user.first_name
    logger.info(f"✏️ Testo da {username} (id={uid}): «{m.text[:60]}»")
    wait = bot.send_message(m.chat.id, "🚀 <b>Generazione in corso...</b>\n⏳ Attendi qualche secondo.")

    def task():
        try:
            sanitized = sanitize_user_input(m.text, gemini)
            raw = generate_monolith_prompt(sanitized)
            reviewed = _architect_review(clean_output(raw))
            certified = clean_output(reviewed)
            try:
                bot.delete_message(m.chat.id, wait.message_id)
            except Exception:
                pass
            if "⚠️ Risposta vuota" in certified or "ERRORE API" in certified:
                bot.send_message(m.chat.id,
                    f"❌ <b>Generazione fallita.</b>\n\n<code>{html.escape(certified)}</code>",
                    reply_markup=get_mode_kb())
                return
            send_prompt(m.chat.id, uid, certified)
            logger.info(f"✅ Prompt testo inviato a {username} ({len(certified)} chars)")
        except Exception as e:
            logger.error(f"❌ task (testo) crash: {e}", exc_info=True)
            try:
                bot.delete_message(m.chat.id, wait.message_id)
            except Exception:
                pass
            bot.send_message(m.chat.id, f"❌ Errore generazione:\n<code>{html.escape(str(e))}</code>")

    executor.submit(task)

