def _architect_review(prompt):
    """Wrapper locale — chiama review_and_fix da shared passando il client."""
    return review_and_fix(prompt, gemini)


# --- INVIO PROMPT ---
def send_prompt(cid, uid, certified, img_bytes=None):
    """Invia il prompt certificato in chunks con pulsanti post-prompt e caption automatica."""
    with _state_lock:
        last_prompt[uid] = certified
    output = f"{EDITORIAL_WRAPPER}\n{certified}"
    chunks = [output[i:i+3800] for i in range(0, len(output), 3800)]
    for idx, chunk in enumerate(chunks):
        header = f"✅ <b>Master Prompt — Architect:</b>\n\n" if idx == 0 else f"<i>(continua {idx+1}/{len(chunks)})</i>\n\n"
        if idx == len(chunks) - 1:
            bot.send_message(cid, f"{header}<code>{html.escape(chunk)}</code>",
                reply_markup=get_after_prompt_kb())
        else:
            bot.send_message(cid, f"{header}<code>{html.escape(chunk)}</code>")
    if img_bytes:
        cap, _ = generate_caption(img_bytes, gemini)
        if cap:
            bot.send_message(cid, cap, parse_mode=None)

