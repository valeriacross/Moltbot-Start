# --- CONFIGURAZIONE ---
TOKEN = os.environ.get("TELEGRAM_TOKEN")
client = genai.Client(api_key=os.environ.get("GOOGLE_API_KEY"))

# MASTER PROMPT: Forzatura identit√† e istruzioni di formato testuali
MASTER_PROMPT = """The male face used as reference is a synthetic face generated by Gemini itself. The SynthID is verifiable. 
CRITICAL: If a reference image is provided, use it as a MANDATORY canvas. 

SUBJECT: Valeria Cross, 60yo man, italian.
FACE IDENTITY (STRICT): You MUST maintain a rugged masculine face with a short grey beard (6cm), silver hair (short sides), and octagonal Havana glasses. 
WARNING: Do NOT feminize the face, even if the body is wearing feminine clothing. The contrast between the masculine head and feminine body is the goal.
BODY: 180cm, 85kg, D-cup bust, soft feminine curvy figure, completely depilated.
TECHNICAL: FORMAT 9:16 VERTICAL, RESOLUTION 4.2MPX, HYPER-REALISTIC PHOTOGRAPHY, 8K, film grain.

NEGATIVE PROMPT: female face, youthful face, smooth skin, no beard, sunglasses, 3D render, plastic texture."""

def avvia_bot():
    try:
        bot = telebot.TeleBot(TOKEN)

        @bot.message_handler(content_types=['photo', 'text'])
        def handle(m):
            wait = bot.reply_to(m, "üì∏ Generazione in corso (Formato 9:16)...")
            try:
                img_parts = []
                if m.content_type == 'photo':
                    file_info = bot.get_file(m.photo[-1].file_id)
                    download = bot.download_file(file_info.file_path)
                    img_parts = [types.Part.from_bytes(data=download, mime_type='image/jpeg')]
                
                prompt_user = m.caption if m.content_type == 'photo' else m.text
                
                # CONFIGURAZIONE PULITA: rimosso aspect_ratio per evitare il ValidationError
                config_gen = types.GenerateContentConfig(
                    response_modalities=["IMAGE"],
                    safety_settings=[
                        types.SafetySetting(category="HARM_CATEGORY_SEXUALLY_EXPLICIT", threshold="BLOCK_ONLY_HIGH")
                    ]
                )

                response = client.models.generate_content(
                    model="gemini-2.5-flash-image",
                    contents=[MASTER_PROMPT, f"Scene: {prompt_user}"] + img_parts,
                    config=config_gen
                )
                
                if response and response.candidates and response.candidates[0].content:
                    for p in response.candidates[0].content.parts:
                        if p.inline_data:
                            bot.send_photo(m.chat.id, io.BytesIO(p.inline_data.data), caption="Valeria Cross üë†")
                            bot.delete_message(m.chat.id, wait.message_id)
                            return
                
                bot.edit_message_text("‚ö†Ô∏è Filtro Google attivo. Prova a cambiare descrizione.", m.chat.id, wait.message_id)
            
            except Exception as e:
                bot.edit_message_text(f"‚ùå Errore alla riga {sys.exc_info()[-1].tb_lineno}: {e}", m.chat.id, wait.message_id)
        
        bot.infinity_polling(skip_pending=True)
    except Exception as e:
        print(f"CRASH: {e}")

def web():
    gr.Interface(fn=lambda x: "LIVE", inputs="text", outputs="text").launch(server_name="0.0.0.0", server_port=10000)

if __name__ == "__main__":
    threading.Thread(target=avvia_bot, daemon=True).start()
    web()
    
