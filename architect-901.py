@bot.message_handler(content_types=['photo'])
def build_prompt_from_image(m):
    cid = m.chat.id
    uid = m.from_user.id
    username = m.from_user.username or m.from_user.first_name

    # ── MODALITÀ MOVIE ────────────────────────────────────────────────────────
    if movie_waiting.pop(uid, False):
        try:
            file_info = bot.get_file(m.photo[-1].file_id)
            img_bytes = bot.download_file(file_info.file_path)
        except Exception as e:
            bot.reply_to(m, f"❌ Errore nel scaricare la foto: {html.escape(str(e))}")
            return
        movie_last_img[uid] = img_bytes
        bot.send_message(cid, "🎬 Genero il prompt Director's Cut 2×2...\n⏳ Attendi qualche secondo.")
        executor.submit(_run_movie, cid, uid, username, img_bytes)
        return
    # ─────────────────────────────────────────────────────────────────────────

    if uid not in user_mode:
        bot.reply_to(m, "⚠️ Seleziona prima un target con /motore")
        return
    try:
        file_info = bot.get_file(m.photo[-1].file_id)
        img_bytes = bot.download_file(file_info.file_path)
        logger.info(f"🖼️ Foto da {username} (id={uid}), {len(img_bytes)} bytes → {user_mode[uid]}")
    except Exception as e:
        logger.error(f"❌ Errore download foto da {username}: {e}")
        bot.reply_to(m, f"❌ Errore nel scaricare la foto: {html.escape(str(e))}")
        return

    engine = user_mode[uid]

    # Aggiunge alla sessione album (esistente o nuova)
    if uid not in album_session:
        album_session[uid] = {'photos': [], 'engine': engine}
    album_session[uid]['photos'].append(img_bytes)
    n = len(album_session[uid]['photos'])
    logger.info(f"   📸 Album uid={uid}: {n} foto")

    markup = InlineKeyboardMarkup()
    if n < 4:
        markup.row(
            InlineKeyboardButton(f"✅ Genera prompt ({n} foto)", callback_data="album_generate"),
            InlineKeyboardButton("➕ Aggiungi foto", callback_data="album_add")
        )
    else:
        markup.row(InlineKeyboardButton(f"✅ Genera prompt ({n} foto — max raggiunto)", callback_data="album_generate"))

    bot.reply_to(m,
        f"📷 Foto {n} ricevuta.\n"
        f"{'Puoi aggiungerne ancora ' + str(4 - n) + '.' if n < 4 else '⚠️ Massimo 4 foto raggiunto.'}",
        reply_markup=markup
    )

@bot.callback_query_handler(func=lambda c: c.data in ["album_generate", "album_add"])
def handle_album_callback(call):
    uid = call.from_user.id
    cid = call.message.chat.id
    try:
        bot.answer_callback_query(call.id)
    except Exception:
        pass

    if call.data == "album_add":
        bot.send_message(cid, "📷 Invia la prossima foto.")
        return

    # album_generate
    session = album_session.pop(uid, None)
    if not session or not session['photos']:
        bot.send_message(cid, "⚠️ Nessuna foto in sessione. Invia una foto.")
        return

    photos = session['photos']
    engine = session['engine']
    n = len(photos)

    if n == 1:
        # Foto singola — comportamento normale
        extra_text = None
        user_last_input[uid] = {'text': None, 'img': photos[0]}
        wait = bot.send_message(cid,
            f"🔍 <b>Analisi immagine per {engine}...</b>\n⏳ Attendi ~30-40 secondi.")
        def task_single():
            bot.send_message(cid, "⚡ Prompt pronto. Usa Flow per generare l'immagine.")
            try:
                bot.delete_message(cid, wait.message_id)
            except Exception:
                pass
        executor.submit(task_single)
    else:
        # Multi-foto — analisi album
        wait = bot.send_message(cid,
            f"🔍 <b>Analisi album ({n} foto) per {engine}...</b>\n⏳ Attendi ~30-40 secondi.")
        def task_album():
            try:
                logger.info(f"   🎬 task_album avviato: {n} foto, engine={engine}")
                raw = generate_from_album(photos, engine)
                logger.info(f"   📝 generate_from_album completato ({len(raw)} chars)")
                reviewed = review_and_fix(raw, engine)
                logger.info(f"   ✅ review_and_fix completato")
                adapted = adapt_to_engine(clean_output(reviewed), engine)
                logger.info(f"   ✅ adapt_to_engine completato")
                ARCH_TAG = "[MASTER PROMPT — ARCHITECT CERTIFIED — SKIP OPTIMIZATION]"
                certified = f"{ARCH_TAG}\n{adapted}"
                try:
                    bot.delete_message(cid, wait.message_id)
                except Exception:
                    pass
                user_last_input[uid] = {'text': certified, 'img': None}
                last_prompt[uid] = certified
                chunks = [certified[i:i+3800] for i in range(0, len(certified), 3800)]
                for idx, chunk in enumerate(chunks):
                    header = f"✅ <b>Master Prompt → {engine} (album {n} foto):</b>\n\n" if idx == 0 else f"<i>(continua {idx+1}/{len(chunks)})</i>\n\n"
                    bot.send_message(cid, f"{header}<code>{html.escape(chunk)}</code>")
                caption = generate_caption(img_bytes=photos[0])
                if caption:
                    bot.send_message(cid, f"{caption}\n#genderfluid #selfexpression #bodypositivity")
                bot.send_message(cid, "🔄 Vuoi riadattarlo per un altro motore o iniziare da zero?", reply_markup=get_after_prompt_kb())
            except Exception as e:
                logger.error(f"   ❌ task_album crash: {e}", exc_info=True)
                try:
                    bot.delete_message(cid, wait.message_id)
                except Exception:
                    pass
                bot.send_message(cid, f"❌ Errore generazione album:\n<code>{html.escape(str(e))}</code>")
        executor.submit(task_album)

