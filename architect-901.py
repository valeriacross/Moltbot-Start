def _build_director_block(name):
    """Returns a detailed cinematic brief for a given director from DIRECTOR_PROFILES."""
    p = DIRECTOR_PROFILES.get(name, {})
    block = f"DIRECTOR: {name}\n"
    if p.get("override"):
        block += f"⚠️ MANDATORY OVERRIDE: {p['override']}\n"
    block += (
        f"COLOR PALETTE: {p.get('color', '')}\n"
        f"CAMERA: {p.get('camera', '')}\n"
        f"LIGHTING: {p.get('lighting', '')}\n"
        f"ATMOSPHERE: {p.get('atmosphere', '')}\n"
        f"REFERENCE FILMS (mandatory, cite these): {p.get('refs', '')}\n"
        f"INTEGRATION RULE: The subject must be fully immersed in this director's visual world — "
        f"not just background-swapped. Outfit picks up colored light from environment. "
        f"Face and body are lit exactly as this director would light them."
    )
    return block

