        f"PANEL 3 (bottom-left, row 2 col 1): Apply ALL specifications above for {panel_assignments[3]} with absolute fidelity. "
        f"Every visual element must match this director's signature style exactly as described.\n\n"
        f"--- PANEL 4 BRIEF ---\n{p4_block}\n\n"
        f"PANEL 4 (bottom-right, row 2 col 2): Apply ALL specifications above for {panel_assignments[4]} with absolute fidelity. "
        f"Every visual element must match this director's signature style exactly as described.\n\n"
        f"Technical requirements: seamless 2x2 grid, no borders or gaps between panels, "
