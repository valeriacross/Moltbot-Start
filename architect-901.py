def analyze_image_visionstruct(img_bytes):
    """Analizza un'immagine con VisionStruct e restituisce un JSON strutturato."""
    import concurrent.futures
    def _call():
        img_part = genai_types.Part.from_bytes(data=img_bytes, mime_type="image/jpeg")
        response = gemini._client.models.generate_content(
            model="gemini-3-flash-preview",
            contents=[VISIONSTRUCT_PROMPT, img_part],
            config=genai_types.GenerateContentConfig(max_output_tokens=1500)
        )
        return response.text.strip() if response.text else None

    try:
        logger.info("   🔍 VisionStruct: analisi immagine in corso...")
        with concurrent.futures.ThreadPoolExecutor(max_workers=1) as ex:
            future = ex.submit(_call)
            raw = future.result(timeout=20)
        if raw:
            raw = re.sub(r'^```json\s*', '', raw)
            raw = re.sub(r'\s*```$', '', raw)
            logger.info(f"   ✅ VisionStruct completato ({len(raw)} chars)")
            return raw
        logger.warning("   ⚠️ VisionStruct: risposta vuota.")
        return None
    except concurrent.futures.TimeoutError:
        logger.warning("   ⚠️ VisionStruct: timeout 20s — salto analisi, procedo senza.")
        return None
    except Exception as e:
        logger.warning(f"   ⚠️ VisionStruct fallito (non bloccante): {e}")
        return None

