    user_last_input[uid] = {'text': m.text, 'img': None}
    wait = bot.send_message(cid, f"🚀 <b>Generazione per {user_mode[uid]}...</b>")
    def task():
        bot.send_message(cid, "⚡ Prompt pronto. Usa Flow per generare l'immagine.")
        try:
            bot.delete_message(cid, wait.message_id)
        except Exception:
            pass
    executor.submit(task)

